      <div class="tab-pane fade show active" id="list-1" role="tabpanel" aria-labelledby="list-1-list">
        <h2>¿Qué es herpes zóster?</h2>

        <p class="fs-4 fw-light mt-4" align="justify">En algún momento de la vida pudimos haber tenido varicela conocida también como chickenpox. El virus que la causa se llama varicela-zóster, después de que la enfermedad pasa, este virus permanece inactivo en el tejido nervioso (en los ganglios) cerca de la médula espinal y el cerebro. Años más tarde, el virus puede reactivarse (se despierta) y viajar a lo largo de los nervios hasta la piel y producir la enfermedad llamada herpes zóster.
        </p>
        <p class="fs-4 fw-light" align="justify">
          Aproximadamente 15% de las personas que tuvieron varicela, va a padecer herpes zóster.<br>
          Al herpes zóster también se le conoce como “Culebrilla”
        </p>
      </div>
      <div class="tab-pane fade" id="list-2" role="tabpanel" aria-labelledby="list-2-list">
        <h2>¿Qué parte del cuerpo afecto el herpes zóster?</h2>

        <p class="fs-4 fw-light mt-4" align="justify">
        El herpes zóster puede manifestarse en cualquier parte del cuerpo, la mayoría de las veces aparece como una franja de ampollas en:
      </p>

      <ul class="fs-4 fw-light">
        <li>Cabeza</li>
        <li>Cara</li>
        <li>Ojos</li>
        <li>Cuello</li>
        <li>Tórax</li>
        <li>Abdomen</li>
        <li>Piernas</li>
        <li>en algún otro lugar.</li>
      </ul>

      </div>
      <div class="tab-pane fade" id="list-3" role="tabpanel" aria-labelledby="list-3-list">
        <h2>¿Qué personas son mas propensas a tener herpes zóster?</h2>

        <p class="fs-4 fw-light mt-4" align="justify">
        Personas con sistemas inmunitarios disminuido presentan con más frecuencia el herpes zóster, por lo regular cursan con enfermedades como:
      </p>

<ul class="fs-4 fw-light">
<li>Diabetes mellitus</li>
<li>Cáncer y sus tratamientos</li>
<li>Desnutrición</li>
<li>Enfermedades avanzadas</li>
<li>Adultos mayores, más común en personas mayores de 50 años</li>
<li>Uso prolongado de esteroides, como la prednisona en enfermedades como la artritis entre otras</li>
<li>Otras enfermedades como VIH/SIDA</li>
</ul>

      </div>
      <div class="tab-pane fade" id="list-4" role="tabpanel" aria-labelledby="list-4-list">
        <h2>¿Cómo saber si es herpes zóster?</h2>

<ul class="fs-4 fw-light mt-4">
<li>Por lo general el herpes zóster, inicia con dolor en la zona que va a manifestarse.</li>
<li>El dolor puede aparecer incluso días antes de que aparezcan las vesículas en la piel, en ocasiones se confunde con otros padecimientos.</li>
<li>Aumento de sensibilidad en la piel, como si estuviera quemada la zona, o como si se hubiera aplicado una crema o algún químico irritante</li>
<li>No hay tolerancia al tacto en la piel, incluso la ropa puede generar molestia (alodinea)</li>
<li>Hormigueo o comezón muy molestos</li>
<li>Por lo general solo aparece en un solo lado de cuerpo</li>
<li>Es conveniente que la persona con posible herpes zóster visite a su médico para que se realice el diagnostico definitivo y reciba el tratamiento adecuado.</li>
</ul>
      </div>
      <div class="tab-pane fade" id="list-5" role="tabpanel" aria-labelledby="list-5-list">
        <h2>Complicaciones por herpes zóster</h2>

        <p class="fs-4 fw-light mt-4" align="justify">
        Los pacientes con dolor grave o incapacitante y una gran cantidad de lesiones tienen menos probabilidades de lograr una resolución del dolor, por eso es importante controlar el dolor lo más temprano posible
      </p>
      <p class="fs-4 fw-light text-primary" align="center">
      Entre más tarde se trate el dolor, más difícil será controlarlo
      </p>
      <p class="fs-4 fw-light text-primary" align="center">
      Afecta la calidad de vida con deficiencias físicas y psicológicas.
      </p>
      </div>
      <div class="tab-pane fade" id="list-6" role="tabpanel" aria-labelledby="list-6-list">
        <h2>¿Como se cura el herpes zóster?</h2>

        <p class="fs-4 fw-light mt-4" align="justify">
        Los objetivos principales del tratamiento del herpes zóster son:
        </p>

<ul class="fs-4 fw-light mt-4">
<li>Inhibir la replicación viral con medicamentos antivirales</li>
<li>Aliviar el dolor agudo</li>
<li>Prevenir las complicaciones, como la neuralgia posherpética</li>
</ul>
<p class="fs-4 fw-light mt-4" align="justify">
Los tratamientos generales para el herpes zóster agudo son:
</p>
<ul class="fs-4 fw-light mt-4">
<li>Una combinación de terapia antiviral</li>
<li>Ciclo corto de corticosteroides al inicio de la enfermedad</li>
<li>Control efectivo del dolor agudo</li>
</ul>

      </div>
      <div class="tab-pane fade" id="list-7" role="tabpanel" aria-labelledby="list-7-list">
        <h2>Neuralgia postherpetica</h2>

        <p class="fs-4 fw-light mt-4" align="justify">
        La neuralgia postherpética es el dolor crónico que se presenta en el trayecto de los nervios que han sido afectados por el herpes zóster, y es la principal complicación por esta enfermedad
        </p>

        <p class="fs-4 fw-light" align="justify">
        Hasta un 34% de las personas que presentan herpes zoster pueden llegar a tener neuralgia postherpética.
        </p>
        <p class="fs-4 fw-light" align="justify">
        El dolor crónico y con mayor intensidad, se desarrolla en casos donde hubo un manejo inadecuado del dolor en la etapa inicial. Afecta la calidad de vida por las deficiencias físicas y psicológicas que conlleva.
        </p>
        <p class="fs-4 fw-light" align="justify">
        El manejo apropiado y oportuno del herpes zoster puede reducir la duración e intensidad del dolor.
        </p>
        <p class="fs-4 fw-light" align="justify">
        La neuralgia posherpética es un dolor neuropático crónico que puede persistir más tiempo, después que las lesiones en piel han cicatrizado o desaparecido y que a menudo es resistente a los tratamientos analgésicos comunes. Es decir los analgésicos de la familia de desinflamatorios como ketorolaco, diclofenaco, etc., no son útiles para este tipo de dolor, en cambio pueden generar graves efectos adversos por la ingesta sin control.
        </p>
      </div>
      <div class="tab-pane fade" id="list-8" role="tabpanel" aria-labelledby="list-8-list">
        <h2>¿Cómo se cura la neuralgia postherpética?</h2>

        <p class="fs-4 fw-light mt-4" align="justify">
        El tratamiento de la neuralgia posherpética a menudo es resistente a los métodos farmacológicos actuales, pues se convirtió en dolor crónico, así que requiere un manejo especial y vigilado por varias semanas incluso años. El médico especialista en dolor (Algólogo) es el experto en este tipo de dolor.
        </p>

<p class="fs-4 fw-light" align="justify">
Es necesario un régimen de tratamiento analgésico multimodal que será ajustado de acuerdo a los antecedentes de cada paciente y a características del dolor, entre estos se puede incluir:
</p>

<ol class="fs-4 fw-light mt-4">
<li>Medicamentos para el control del dolor del sistema nervioso, llamados neuromoduladores, algunos ejemplos son: Pregabalina, gabapentina, carbamazepina, Duloxetina, Imipramina, etc.
</li>
<li>Anestésicos locales como la lidocaína</li>
<li><a href="<?=LINK_TRATAMIENTO_OPIOIDES;?>">Opioides</a> cuando el dolor es moderado a severo y no se controla con las primeras opciones</li>
<li>Hay otros tratamientos intervencionistas (bloqueos) para aliviar el dolor</li>
<li><a href="<?=LINK_NEUROESTIMULACION;?>">Neuroestimulador.</a> Cuando el dolor no se controla con medicamentos o no los tolera el paciente por los efectos adversos importantes, puede ser útil colocar un neuroestimulador</li>
</ol>
<p class="fs-4 fw-light" align="justify">
La vacunación profiláctica contra el herpes zóster puede prevenir o reducir la incidencia de herpes zóster y de neuralgia posherpética.
</p>
<p class="fs-4 fw-light" align="justify">
La neuralgia posherpética (NPH) es un dolor neuropático crónico de difícil control y su tratamiento debe ser bajo un régimen estricto de medicamentos que tienen efectos adversos que se pueden prevenir y llevar bien si se tiene un tratamiento y vigilancia por el especialista en dolor más cuidados por la familia cuando es necesario. Incluso se puede disminuir o suspenderse la medicación en forma progresiva si el paciente mejora.
</p>
