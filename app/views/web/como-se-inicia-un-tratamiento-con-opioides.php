           <p class="fs-4 fw-light mt-4" align="justify">
          Náusea y vómito: La náusea o vómito puede o no presentarse con los opioides, aunque si el paciente presenta algunos antecedentes como gastritis, deshidratación, vértigo, quimioterapia, ingesta de otros fármacos que generen náusea o vomito, el efecto puede ser mas intenso. Es recomendable indicar medicamentos para prevenirlo. El paciente puede hacer tolerancia (se acostumbra al medicamento) en 2 a 3 semanas.<br><br>

          Somnolencia: Este síntoma es común durante los siguientes días de comenzar el tratamiento. Con frecuencia el paciente deja de presentarlo en dos semanas de iniciado el tratamiento. El paciente no debe manejar un automóvil o realizar actividades que requieran de su completa atención y reflejos, pues pone en riesgo la vida.
