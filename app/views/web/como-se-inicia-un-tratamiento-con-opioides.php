        <h2>Uso de rescates o dosis extras</h2>

        <p class="fs-4 fw-light mt-4" align="justify">
        Los recates se empiezan a utilizar una vez que se ha ajustado la dosis que el paciente necesita. Es requisito tener la titulación del opioide antes de usar un rescate.<br><br>

        Los rescates se usan cuando a pesar de tener un medicamento opioide con horario fijo, existen picos de dolor desencadenados por alguna situación, a este dolor se le conoce como dolor irruptivo o incidental y puede desencadenarse por haber hecho más actividad de lo normal, procedimientos, cambios de temperatura, etc.<br><br>

        Por lo general los rescates están indicados en el dolor por cáncer.
        </p>

        <div class="border p-2">
        <p class="fs-4 fw-light mt-4" align="center">
        Su médico le indicará:<br>
        En caso de dolor no controlado por el analgésico con horario, administrar:<br>
        __ Opioide___ de _X mgs__<br>
        Usted podrá tomar máximo de __X___ rescates de __su opioide__ al día.
        </p>
        </div>

