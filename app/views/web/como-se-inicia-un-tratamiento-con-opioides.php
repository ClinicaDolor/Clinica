       <h2>Pasos para utilizar un opioide</h2> 

       <p class="fs-4 fw-light mt-4" align="justify">
       El manejo de dolor con opioides deberá ser llevado a cabo por un médico(s) que tiene la preparación, experiencia y que pueda llevar un seguimiento continuo desde el inicio hasta el final del tratamiento con estos medicamentos, ya sea para dolor agudo donde puede requerir sólo unas horas para iniciar y retirar el fármaco o en el caso de dolor crónico puede requerir días o semanas para ajustar dosis y años para el seguimiento y ajustes de las dosis y en ocasiones el retiro. Aquí mencionamos solo algunos pasos que son necesarios para dar un tratamiento con opioides.
     </p>

      <ul class="fs-4 fw-light mt-4"> 
      <li>Conocer los antecedentes (una historia clínica) del paciente como;
      <ul>
      <li>Enfermedades previas</li>
      <li>Alergias</li>
      <li>Enfermedad actual</li>
      <li>Causa que genera el dolor y su probable tratamiento</li>
      <li>Riesgos de efectos adversos que pueden presentarse con cualquier analgésico.</li>
      <li>Experiencias previas con opioides, sedantes, o algunas sustancias que generan sueño o sedación.</li>
      </ul>
      <li>Evaluar la intensidad y tipo de dolor del paciente, pues no todos los dolores recibirán el mismo tratamiento
      <li>Conocer las posibles reacciones del paciente ante el efecto del analgésico opioide.
      <li>¿Cómo prevenir los efectos adversos o consecuencias por la toma de opioides?
      <li>Conocer qué se debe hacer en caso de algún efecto adverso.
      <li>Titular (encontrar la dosis) la dosis del opioide que se considere ser el más indicado para el tipo de paciente, tipo de dolor y tiempo que se requiera el tratamiento.
      </ul>

