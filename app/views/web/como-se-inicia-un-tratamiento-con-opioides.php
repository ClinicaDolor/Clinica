        <h2>Tabletas, gotas, inyecciones, parches para el dolor</h2>
        <p class="fs-4 fw-light mt-4" align="justify">
        Los pacientes que pueden beneficiarse de la administración de opioides y las vías de administración pueden ser:
      </p>

      <ol class="fs-4 fw-light mt-4"> 
      <li>Tomadas conocido como via oral: como gotas, cápsulas o tabletas; hay tabletas de acción rápida y también de liberación prolongada.</li>
      <li>Inyectable o Parenteral: Vía subcutánea o intravenosa</li>
      <li>Parches o transdérmica: Los parches de morfina no existen. Los parches de opioides que hay en le mercado son de Fentanilo y de Buprenorfina, ambos son opioides potentes. Los parches tardan 12 a 24 horas para alcanzar un efecto analgésico.</li>
      </ol>

      <p class="fs-4 fw-light mt-4" align="justify">
      La vía por la que se administrara el opioide puede depender de varios factores entre los cuales será:
      </p>

<ul class="fs-4 fw-light mt-4">
<li>Si el paciente puede o quiere ingerir tabletas, capsulas o gotas</li>
<li>Si el paciente tiene vómito, no va a retener el medicamento</li>
<li>Si el paciente esta en ayuno para una cirugía tal vez será posible ingerir su medicamento con un trago de agua unidamente.</li>
<li>Si es indispensable un ayuno sin tabletas, tal vez las gotas sublinguales podrían ser una opción</li>
<li>Si el paciente será sometido a una cirugía o procedimiento, seguramente el anestesiólogo adminsitrará el opioide vía intravenosa</li>
<li>Si se trata de dolor crónico, en pacientes estables y cuando queremos evitar ingesta constante de tabletas, los parches (vía transdérmica) son una excelente opción, incluso para mantener más constantes las dosis del medicamento</li>
</ul>

