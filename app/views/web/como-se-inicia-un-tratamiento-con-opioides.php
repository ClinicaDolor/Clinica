    <p class="fs-4 fw-light mt-4" align="justify">
    Otras causas de sedación: Es frecuente que un paciente presente mas de una causa para sentirse con sueño o sedado.
    </p>

    <ol class="fs-4 fw-light">
      <li>Insomnio o no descanso por dolor. El dolor es una de las causas por las que un paciente no puede conciliar el sueño o el dolor despierta al paciente y no le permite descansar adecuadamente, si esto pasa durante días o semanas,  en el momento que el dolor se controla, el cuerpo va a querer recuperar las horas de sueño faltante. </li>
      <li>Medicamentos para dormir: Es común que una persona tome medicamentos como clonazepam, midazolam, relajantes musculares como; tizanidina, clorsaxozona, etc., tés, o somníferos. Cuando se inicia con opioides debe considerarse que estos pueden sumarse a los efectos de los otros medicamentos que causan sueño o sedación, así que deben de hacerse ajustes o recomendaciones para evitar las interacciones.  </li>
      <li>Enfermedades: Algunas enfermedades de base pueden ser causa de debilidad, fatiga y somnolencia. Ejemplos de algunas enfermedades puede ser la desnutrición, anemia, asma, edema agudo de pulmón (agua en los pulmones), neumonía, cancer,  entre otros. Algunas de estas enfermedades pueden ser la causa de fatiga y sueño, sumado a esto un opioide puede generar mas sueño y sedación, motivo por el que el médico debe indicar con precaución el analgésico, ademas el decidirá la forma de vigilancia del paciente y si es conveniente hospitalizarlo para titular la analgesia con opioides</li>
