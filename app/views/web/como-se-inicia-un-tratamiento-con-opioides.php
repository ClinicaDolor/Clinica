      <li>Tratamiento para las náuseas y/o vómitos causados por los opioides</li>
      <li>Las náuseas y vómitos ocasionados por los opioides normalmente son transitorios y suelen controlarse con antieméticos (medicamentos para nausea o vómito), estos medicamentos pueden indicarse de rutina para prevenir este efecto.</li>
      <li>Cuando el vómito generado por opioides no se puede controlar con medicamentos antieméticos orales, se debe utilizar antiemeticos inyectables al menos hasta que el vómito se controle. Un paciente lábil puede deshidratarse por vómito no controlado, incluso llegar a la deshidratación con disminución de la presión arterial y agravar el cuadro. Hay pacientes que tiene mas de un factor de riesgo para presentar náusea o vómito, es entonces que tal vez necesiten de mas de un medicamento antiemético para prevenir o tratar este efecto</li>
      <li>El tratamiento para las náuseas o vómito puede ser:</li>
      <li>Antieméticos; medicamentos para evitar el vómito como ondansetron, palonosetron, metoclopramida, difenidol, entre otros</li>
      <li>Hidratación oral, intravenosa o subcutánea</li>
      <li>Cambiar o suspender los opioides por unas horas mientras se controla el vómito</li>
      <li>Cuando los opioides se necesitan por tiempo prolongado se recomienda considerar uno o varios medicamentos para evitar el vómito al menos mientras hay tolerancia a la náusea o vómito. Algunos pacientes puede hacer tolerancia a los efectos secundarios (se acostumbra al medicamento) en 2 a 3 semanas, por lo que es probable que durante este tiempo se debe ingerir los medicamentos para prevenir las náuseas y vómito.</li>
