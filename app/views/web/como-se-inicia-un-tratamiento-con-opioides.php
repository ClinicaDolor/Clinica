        <p class="fw-light"><small>Rescates: Dosis extra de opioide para controlar el dolor esporádico o al movimiento, para controlar el dolor que los opioides con horario no logran controlar.</small></p>

        <p class="fs-4 fw-light mt-4" align="justify">
        Se debe hacer uso responsable de los rescates. El paciente no debe aguantar el dolor en forma innecesaria, para eso están indicados los rescates. Tampoco hay que abusar de ellos.<br><br>

        Los rescates también se pueden utilizar antes de que se vaya a realizar una actividad que causa dolor, ejemplo: cuando hay que cambiar la ropa de cama de un paciente y el movimiento genera dolor intenso.<br><br>

        El paciente y cuidador, deben conocer la dosis y frecuencia de cada rescate, así como los posibles efectos esperados para platicarlo con su médico y poder hacer un esquema a la medida de las necesidades durante la evolución de la enfermedad.<br><br>

        Una vez que se conoce la forma y beneficio del rescate debe de utilizarse con prudencia, dentro del balance de evitar el dolor y evitar el abuso. En las consultas médicas su especialista en dolor le ira asesorando sobre el mejor método para lograr el objetivo mencionado.
        </p>

