        <h2>Titulación, ajuste y mantenimiento</h2>

        <p class="fs-4 fw-light mt-4" align="justify">
        La titulación es el proceso en el que se decide el mejor opioide para el paciente y su enfermedad así como la dosis, ya que estos medicamentos no tienen una dosis establecida que sea igual para todos. La dosis será la que cada paciente necesite, vigilando el equilibrio entre adecuado control del dolor y mínimos efectos adversos.<br><br>

        En dolor agudo, la titulación se hace en forma rápida, con monitores y vigilancia medica continua.<br><br>

        En dolor crónico inicialmente, los ajustes se harán en días y semanas hasta lograr un adecuado control de dolor. En paciente ancianos, débiles o con deterioro cognitivo, la vigilancia deberá ser por sus familiares o cuidadores. Además deberán informar a su médico (quien hizo la prescripción) sobre la evolución del paciente y acudir a las citas indicadas para los ajustes requeridos.
        </p>


